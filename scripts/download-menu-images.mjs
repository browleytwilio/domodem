import { readFileSync, writeFileSync, mkdirSync, existsSync, statSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const args = new Set(process.argv.slice(2));
const DRY_RUN = args.has("--dry-run");
const CONCURRENCY = 4;

const MENU_WIDTH = 800;
const DEAL_WIDTH = 1200;
const HERO_WIDTH = 1920;
const TOPPING_SIZE = 400;
const CRUST_WIDTH = 400;
const CRUST_HEIGHT = 300;
const CATEGORY_SIZE = 200;
const STORE_WIDTH = 800;
const STORE_HEIGHT = 500;

const MISC_SIZES = {
  "app-mockup": { width: 400, height: 820 },
  "auth-hero":  { width: 1200, height: 1600 },
  "empty-cart": { width: 600, height: 600 },
  "error":      { width: 800, height: 600 },
  "og-image":   { width: 1200, height: 630, extraFormats: ["png"] },
};

function log(line) {
  process.stdout.write(`${line}\n`);
}

function unsplashUrl(id, width) {
  return `https://images.unsplash.com/photo-${id}?w=${width * 2}&q=82&auto=format&fit=crop&fm=jpg`;
}

async function fetchBuffer(id, width) {
  const url = unsplashUrl(id, width);
  const res = await fetch(url, {
    headers: { "User-Agent": "domdemo-image-downloader/1.0" },
  });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return Buffer.from(await res.arrayBuffer());
}

async function writeProcessed(buf, outputPath, { width, height, format = "webp" }) {
  const dir = dirname(outputPath);
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });

  const pipeline = sharp(buf).resize({
    width,
    height,
    fit: "cover",
    position: "center",
    withoutEnlargement: false,
  });

  if (format === "png") {
    await pipeline.png({ quality: 90 }).toFile(outputPath);
  } else {
    await pipeline.webp({ quality: 82 }).toFile(outputPath);
  }
}

async function downloadOne({ slug, id, fallbackIds, outputPath, width, height, label, extraFormats }) {
  if (DRY_RUN) {
    log(`  [dry] ${label} → ${outputPath} (id=${id})`);
    return { ok: true, slug, skipped: true };
  }

  const candidates = [id, ...(fallbackIds ?? [])].filter(Boolean);
  let lastErr;

  for (const candidate of candidates) {
    try {
      const buf = await fetchBuffer(candidate, width);
      await writeProcessed(buf, outputPath, { width, height });

      if (extraFormats?.includes("png")) {
        const pngPath = outputPath.replace(/\.webp$/, ".png");
        await writeProcessed(buf, pngPath, { width, height, format: "png" });
      }

      const size = statSync(outputPath).size;
      const usedFallback = candidate !== id ? " (fallback)" : "";
      log(`  done: ${label}${usedFallback} → ${outputPath} (${(size / 1024).toFixed(1)} KB)`);
      return { ok: true, slug, usedFallback: candidate !== id };
    } catch (err) {
      lastErr = err;
    }
  }

  log(`  FAIL: ${label} — ${lastErr?.message || "unknown error"}`);
  return { ok: false, slug, error: lastErr?.message };
}

async function runBatched(tasks) {
  const results = [];
  for (let i = 0; i < tasks.length; i += CONCURRENCY) {
    const slice = tasks.slice(i, i + CONCURRENCY);
    const batch = await Promise.all(slice.map((t) => downloadOne(t)));
    results.push(...batch);
  }
  return results;
}

function buildCreditsContent(manifest, failures) {
  const lines = [
    "# Image credits",
    "",
    "All product, deal, topping, store, and misc imagery is sourced from royalty-free photographers on Unsplash.",
    "This file is auto-generated by `scripts/download-menu-images.mjs`.",
    "",
    "## Menu",
    "",
  ];

  for (const [slug, entry] of Object.entries(manifest.menu)) {
    lines.push(`- \`${slug}\` — ${entry.credit}`);
  }

  lines.push("", "## Deals", "");
  for (const [id, entry] of Object.entries(manifest.deals)) {
    lines.push(`- \`${id}\` — ${entry.credit}${entry.note ? ` _(${entry.note})_` : ""}`);
  }

  lines.push("", "## Hero", "");
  manifest.hero.forEach((entry, idx) => {
    lines.push(`- slide ${idx + 1} — ${entry.credit}${entry.note ? ` _(${entry.note})_` : ""}`);
  });

  if (manifest.toppings) {
    lines.push("", "## Toppings & sauces", "");
    for (const [slug, entry] of Object.entries(manifest.toppings)) {
      lines.push(`- \`${slug}\` — ${entry.credit}`);
    }
  }

  if (manifest.crusts) {
    lines.push("", "## Crusts", "");
    for (const [slug, entry] of Object.entries(manifest.crusts)) {
      lines.push(`- \`${slug}\` — ${entry.credit}${entry.note ? ` _(${entry.note})_` : ""}`);
    }
  }

  if (manifest.categories) {
    lines.push("", "## Menu categories", "");
    for (const [slug, entry] of Object.entries(manifest.categories)) {
      lines.push(`- \`${slug}\` — ${entry.credit}`);
    }
  }

  if (manifest.stores) {
    lines.push("", "## Stores", "");
    for (const [id, entry] of Object.entries(manifest.stores)) {
      lines.push(`- \`${id}\` — ${entry.credit}${entry.note ? ` _(${entry.note})_` : ""}`);
    }
  }

  if (manifest.misc) {
    lines.push("", "## Miscellaneous", "");
    for (const [slug, entry] of Object.entries(manifest.misc)) {
      lines.push(`- \`${slug}\` — ${entry.credit}${entry.note ? ` _(${entry.note})_` : ""}`);
    }
  }

  if (failures.length > 0) {
    lines.push("", "## Failed downloads", "");
    for (const f of failures) {
      lines.push(`- \`${f.slug}\` — ${f.error}`);
    }
  }

  return lines.join("\n") + "\n";
}

function writeCredits(manifest, failures) {
  const outputPath = join(root, "public/images/CREDITS.md");
  writeFileSync(outputPath, buildCreditsContent(manifest, failures), "utf-8");
  log(`\nwrote ${outputPath}`);
}

async function writeAppleTouchIcon(manifest) {
  const entry = manifest.menu.pepperoni;
  if (!entry) return;
  try {
    const buf = await fetchBuffer(entry.unsplashId, 180);
    const outputPath = join(root, "public/images/apple-touch-icon.png");
    await writeProcessed(buf, outputPath, { width: 180, height: 180, format: "png" });
    log(`  done: apple-touch-icon → ${outputPath}`);
  } catch (err) {
    log(`  FAIL: apple-touch-icon — ${err.message}`);
  }
}

async function main() {
  log(`Downloading imagery${DRY_RUN ? " (dry run)" : ""}...\n`);

  const manifest = JSON.parse(readFileSync(join(root, "scripts/image-manifest.json"), "utf-8"));
  const menuData = JSON.parse(readFileSync(join(root, "src/data/menu.json"), "utf-8"));
  const dealsData = JSON.parse(readFileSync(join(root, "src/data/deals.json"), "utf-8"));
  const toppingsData = JSON.parse(readFileSync(join(root, "src/data/toppings.json"), "utf-8"));
  const storesData = JSON.parse(readFileSync(join(root, "src/data/stores.json"), "utf-8"));

  const tasks = [];

  for (const item of menuData) {
    const entry = manifest.menu[item.slug];
    if (!entry) { log(`  warn: no menu manifest for "${item.slug}"`); continue; }
    tasks.push({
      slug: item.slug,
      id: entry.unsplashId,
      fallbackIds: [manifest.fallbacks[item.category]],
      outputPath: join(root, "public", item.image.replace(/^\//, "")),
      width: MENU_WIDTH, height: MENU_WIDTH,
      label: `menu/${item.slug}`,
    });
  }

  for (const deal of dealsData) {
    const entry = manifest.deals[deal.id];
    if (!entry) { log(`  warn: no deal manifest for "${deal.id}"`); continue; }
    tasks.push({
      slug: deal.id,
      id: entry.unsplashId,
      fallbackIds: [manifest.fallbacks.deals],
      outputPath: join(root, "public", deal.image.replace(/^\//, "")),
      width: DEAL_WIDTH, height: DEAL_WIDTH,
      label: `deal/${deal.id}`,
    });
  }

  manifest.hero.forEach((entry, idx) => {
    tasks.push({
      slug: `hero-${idx}`,
      id: entry.unsplashId,
      fallbackIds: [manifest.fallbacks.hero],
      outputPath: join(root, `public/images/hero/hero-${idx}.webp`),
      width: HERO_WIDTH, height: Math.round(HERO_WIDTH * 0.5),
      label: `hero/slide-${idx}`,
    });
  });

  for (const topping of toppingsData) {
    const entry = manifest.toppings?.[topping.slug];
    if (!entry) { log(`  warn: no topping manifest for "${topping.slug}"`); continue; }
    const catFallback = entry.fallbackCategory
      ? manifest.fallbacks[entry.fallbackCategory]
      : manifest.fallbacks[topping.category];
    tasks.push({
      slug: topping.slug,
      id: entry.unsplashId,
      fallbackIds: [catFallback, manifest.fallbacks.categories].filter(Boolean),
      outputPath: join(root, "public", topping.image.replace(/^\//, "")),
      width: TOPPING_SIZE, height: TOPPING_SIZE,
      label: `topping/${topping.slug}`,
    });
  }

  if (manifest.crusts) {
    for (const [slug, entry] of Object.entries(manifest.crusts)) {
      tasks.push({
        slug: `crust-${slug}`,
        id: entry.unsplashId,
        fallbackIds: [manifest.fallbacks.crusts],
        outputPath: join(root, `public/images/crusts/${slug}.webp`),
        width: CRUST_WIDTH, height: CRUST_HEIGHT,
        label: `crust/${slug}`,
      });
    }
  }

  if (manifest.categories) {
    for (const [slug, entry] of Object.entries(manifest.categories)) {
      tasks.push({
        slug: `category-${slug}`,
        id: entry.unsplashId,
        fallbackIds: [manifest.fallbacks.categories],
        outputPath: join(root, `public/images/categories/${slug}.webp`),
        width: CATEGORY_SIZE, height: CATEGORY_SIZE,
        label: `category/${slug}`,
      });
    }
  }

  if (manifest.stores) {
    for (const store of storesData) {
      const entry = manifest.stores[store.id];
      if (!entry) { log(`  warn: no store manifest for "${store.id}"`); continue; }
      const imagePath = store.image || `/images/stores/${store.id}.webp`;
      tasks.push({
        slug: store.id,
        id: entry.unsplashId,
        fallbackIds: [manifest.fallbacks.stores],
        outputPath: join(root, "public", imagePath.replace(/^\//, "")),
        width: STORE_WIDTH, height: STORE_HEIGHT,
        label: `store/${store.id}`,
      });
    }
  }

  if (manifest.misc) {
    for (const [slug, entry] of Object.entries(manifest.misc)) {
      const size = MISC_SIZES[slug] || { width: 800, height: 600 };
      tasks.push({
        slug: `misc-${slug}`,
        id: entry.unsplashId,
        fallbackIds: [manifest.fallbacks.misc],
        outputPath: join(root, `public/images/${slug}.webp`),
        width: size.width, height: size.height,
        extraFormats: size.extraFormats,
        label: `misc/${slug}`,
      });
    }
  }

  log(`Queued ${tasks.length} images\n`);

  const results = await runBatched(tasks);

  if (!DRY_RUN) await writeAppleTouchIcon(manifest);

  const failures = results.filter((r) => !r.ok);
  const fallbackCount = results.filter((r) => r.ok && r.usedFallback).length;

  if (!DRY_RUN) writeCredits(manifest, failures);

  log("");
  log(`Summary: ${results.length - failures.length}/${results.length} succeeded${fallbackCount ? `, ${fallbackCount} used fallback` : ""}`);
  if (failures.length > 0) {
    log(`Failures: ${failures.map((f) => f.slug).join(", ")}`);
    process.exit(1);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
